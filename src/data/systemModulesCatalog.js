export function getModuleById(id) {
  return systemModulesCatalog.find((module) => module.id === id);
}

export function getModulesByGroup(modules = systemModulesCatalog) {
  return modules.reduce((groups, module) => {
    if (!groups[module.group]) groups[module.group] = [];
    groups[module.group].push(module);
    return groups;
  }, {});
}

export function getProposalModulesDefault() {
  return systemModulesCatalog.map((module) => module.id);
}
